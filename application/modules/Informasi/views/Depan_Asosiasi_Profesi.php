								Asosiasi Profesi adalah organisasi berbadan hukum yang menaungi sekelompok orang yang memiliki profesi dan keahlian yang sama serta memiliki tujuan yang sama dalam pembinaan dan pengembangan praktik berprofesi.<br><br> 
								Asosiasi profesi memiliki tanggung jawab untuk membina, melindungi dan mengembangkan anggota  dan keprofesiannya. Dalam penyelenggaraan bangunan gedung, Asosiasi profesi memiliki peran untuk melalukan verifikasi calon anggota TPA yang berasal dari asosiasi tersebut pada berbagai tingkatan.<br><br>
								Asosiasi profesi dibagi menjadi 3 tingkatan berdasarkan wilayah administratifnya, yaitu;
										<ul><br>
											<li>Asosiasi Pusat</li>
											<li>Asosiasi Provinsi</li>
											<li>Asosiasi Kabupaten Kota</li>
										</ul>
								Asosiasi pusat berkedudukan paling tinggi dan bertanggung jawab atas pembinaan, perlindungan, dan pengembangan anggota dan keprofesiannya di seluruh Indonesia. Asosiasi pusat berkedudukan pada kantor pusat asosiasi dan berperan melakukan verifikasi atas calon anggota TPA di tingkat pusat.<br><br>
								Asosiasi Provinsi berkedudukan di wilayah provinsi dan bertanggung jawab atas pembinaan, perlindungan, dan pengembangan anggota dan keprofesiannya di wilayah administratifnya. Asosiasi Provinsi berkedudukan pada kantor cabang asosiasi di tingkat provinsi dan berperan melakukan verifikasi atas calon anggota TPA di tingkat provinsi.<br><br>
								Asosiasi Kabupaten/Kota berkedudukan di wilayah Kabupaten/Kota dan bertanggung jawab atas pembinaan, perlindungan, dan pengembangan anggota dan keprofesiannya di wilayah administratifnya. Asosiasi Provinsi berkedudukan pada kantor cabang asosiasi di tingkat Kabupaten/Kota dan berperan melakukan verifikasi atas calon anggota TPA di tingkat Kabupaten/Kota.
									
