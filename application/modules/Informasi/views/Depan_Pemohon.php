										<p>
											<b>Pemohon</b> adalah perorangan atau badan yang mengajukan permohonan terkait PBG, SLF, SBKBG, RTB atau yang ingin mendatakan bangunannya. Pemohon harus menggunakan SIMBG untuk melakukan proses Penyelenggaraan Bangunan Gedung.
										</p>
										Dalam pelaksanaan proses penyelenggaraan Bangunan Gedung dengan SIMBG, Pemohon bertanggung jawab untuk:
										
											<ul><br>
												<li>Melengkapi data dan dokumen teknis permohonan </li>
												<li>Menghadiri konsultasi perencanaan dan/atau pembongkaran Bangunan Gedung (bila diperlukan)</li>
												<li>Membayar retribusi daerah yang telah ditetapkan (bila ditagihkan) </li>
												<li>Menyampaikan informasi jadwal dan tanggal mulai pelaksanaan konstruksi kepada Dinas Teknis melalui SIMBG pada tahap pembangunan Bangunan Gedung </li>
												<li>Menyampaikan informasi jadwal dan tanggal mulai pembongkaran kepada Dinas Teknis melalui SIMBG pada tahap pembongkaran Bangunan Gedung </li>
												<li>Mendaftarkan akun perencana konstruksi, pelaksana konstruksi, perencana pembongkaran dan pelaksana pembongkaran (bila diperlukan) </li>
											</ul>
										<hr>
										Anda dapat mendaftar menjadi Pemohon dan mengajukan permohonnan terkait PBG, SLF, SBKBG, RTB atau yang ingin mendatakan bangunannya dengan melengkapi informasi yang harus diisi pada halaman web SIMBG:
											<ul> <br>
												<li>Data Pemohon atau Pemilik </li>
												<li>Data Bangunan Gedung </li>
												<li>Dokumen rencana teknis </li>
