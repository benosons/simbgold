								Kepala Dinas Teknis adalah Kepala Dinas yang memimpin dinas teknis yang mengurusi urusan bangunan gedung
								di wilayah administratifnya. Kepala Dinas Teknis berstatus Aparatur Sipil Negara dengan Tingkat Eselon
								III. <br><br> Kepala Dinas Teknis bertugas untuk membantu Menteri Pekerjaan Umum dan Perumahan Rakyat
								dalam penyelenggaraan dan pelaksanaan pembangunan Bangunan Gedung di wilayah administratifnya. <br><br>
								Dalam Penyelenggaraan Persetujuan Bangunan Gedung (PBG), Kepala Dinas Teknis bertanggung jawab untuk:
								<ul>

									<li>mengeluarkan surat pernyataan pemenuhan standar teknis.</li>
									<li>menerbitkan SLF dan SBKBG</li>
									<li>menunjuk Pengawas dari organ dibawahnya yang akan mengawasi keberjalanan pelayanan pengurusan PBG
									</li>
									<li>membuat Akun SIMBG untuk Pengawas yang ditunjuk </li>
								</ul>

								<b>
									<hr></b>

								Untuk memperoleh akun, Kepala Dinas Teknis mengajukan permohonan perihal pembuatan akun Kepala Dinas
