								<ul>
									<li>Nama Akun</li>
									<li>Password Akun</li>
								</ul>
								Selanjutnya, Kepala Dinas Teknis dapat mengganti password akun pada SIMBG sesuai kebutuhan. untuk
								mengganti password akun Kepala Dinas Teknis, klik menu Profil lalu pilih Ubah Kata Sandi.<br><br>

