											<div id="accordion1_2" class="panel-collapse collapse">
												<div class="panel-body">
													 Sertifikat Laik Fungsi, atau disingkat SLF, adalah sertifikat yang dikeluarkan dari pemerintah kepada pemilik sebuah bangunan gedung atau perwakilannya sebagai pernyataan bahwa bangunan yang bersangkutan laik fungsi dan dapat digunakan dengan benar sesuai rencana.<br><br>
													 SLF dapat diterbitkan apabila kondisi bangunan yang diajukan memenuhi standar teknis sesuai ketentuan peraturan perundang-undangan, untuk mengetahui apakah bangunan tersebut memenuhi standar teknis atau tidak, diperlukan sebuah proses pemeriksaan dan inspeksi yang melibatkan tenaga ahli yang memiliki kemampuan dan keahlian terkait bangunan gedung.<br><br>
													 Tenaga ahli yang dimaksud berasal dari keprofesian yang memiliki kemampuan dan keahlian untuk melaksanakan pemeriksaan fisik, atau dari Pemerintah yang memiliki kemampuan serupa.<br><br>
													 SLF memiliki fungsi:
													 <ul><br>
														<li>Memastikan bangunan gedung aman untuk digunakan.</li>
														<li>Memastikan bangunan gedung tersebut memenuhi standar yang menjamin keselamatan, kenyamanan, kesehatan, dan kemudahan bagi penggunanya.</li>
														<li>Mendata keberadaan fisik bangunan gedung.</li>
													</ul>
													SLF dikeluarkan oleh pemerintah sesuai kewenangannya dan dikeluarkan setelah semua aspek bangunan diperiksa dan dinilai lulus uji.<br><br>
													Aspek yang diperiksa meliputi:<br><br>
													&emsp;&emsp;1.  Struktur <br>
													&emsp;&emsp;2.  Arsitektur <br>
													&emsp;&emsp;3.  Mechanical Electrical Plumbing (MEP) <br><br>
													untuk memulai mengajukan SLF <a data-toggle="modal" data-target="#Loginnya"><b>klik disini</b></a> <br><br>
													SLF berlaku secara periodik, dengan usia SLF ini adalah
													<ul><br>
														<li>20 tahun untuk bangunan rumah tinggal.</li>
														<li>5 tahun untuk bangunan lainnya.</li>
													</ul>
													apabila sudah habis masa berlakunya, SLF harus diperpanjang sebelum bangunan bisa digunakan kembali. untuk memulai mengajukan perpanjangan SLF, <a data-toggle="modal" data-target="#Loginnya"><b>klik disini</b></a>
