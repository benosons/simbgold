<script language="javascript" type="text/javascript">
function popupIMB() {
	var no_registrasi = document.getElementById("imb").value;
	if(no_registrasi == ""){
		alert("Silahkan Mengisi Nomor Registrasi Dengan Benar");
		location.reload();
	}else{
		$.ajax({
			type: "get",
			url:  "<?php echo base_url();?>Informasi/Lacak/"+no_registrasi,
			data: $('form.form-horizontal').serialize(),
			success: function(response) {
				if(response == ""){
					alert("Nomor Registrasi Tidak Ditemukan");
					document.getElementById('imb_list').style.display="none";
				}else{
					$('#table_IMB tbody').html(response);
					document.getElementById('imb_list').style.display="block";	
				}
			},
			error: function(error) {
				alert('Nomor Registrasi Tidak Ditemukan');
				location.reload();
			}
		});
	}
}
</script>
