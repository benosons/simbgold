											<div id="accordion1_1" class="panel-collapse collapse in">
												<div class="panel-body">
													 Persetujuan Bangunan Gedung, atau disingkat PBG, adalah Perizinan yang dikeluarkan dari pemerintah kepada pemilik sebuah bangunan gedung atau perwakilannya untuk memulai pembangunan, merenovasi, merawat, atau mengubah bangunan gedung tersebut sesuai dengan yang direncanakan.<br><br>
													 PBG dapat diterbitkan apabila rencana teknis yang diajukan memenuhi standar teknis sesuai ketentuan peraturan perundang-undangan, untuk mengetahui apakah rencana teknis tersebut memenuhi standar teknis atau tidak, diperlukan sebuah proses konsultasi yang melibatkan tenaga ahli yang memiliki kemampuan dan keahlian terkait bangunan gedung.<br><br>
													 Tenaga ahli yang dimaksud dapat berasal dari keprofesian, maupun dari perguruan tinggi. untuk mengetahui siapa saja yang bisa memeriksa rencana teknis tersebut, <a data-toggle="modal" data-target="#Loginnya"><b>klik disini</b></a>
													 <br><br>
													 PBG memiliki fungsi:
													 <ul><br>
														<li>Memastikan pembangunan bangunan gedung berstatus legal.</li>
														<li>Memastikan penyelenggaraan bangunan gedung tersebut memenuhi standar yang menjamin keselamatan, kenyamanan, kesehatan, dan kemudahan bagi penggunanya.</li>
														<li>Mendata keberadaan rencana bangunan gedung.</li>
													</ul>
													
													PBG dikeluarkan oleh pemerintah sesuai kewenangannya dan dikeluarkan paling lambat 28 hari kerja, tergantung fungsi dan klasifikasi bangunannya.<br><br>
													Proses yang dilakukan dalam 28 hari tersebut meliputi:<br><br>
													&emsp;&emsp;1.  Pengajuan <br>
													&emsp;&emsp;2.  Pemeriksaan Rencana Teknis <br>
													&emsp;&emsp;3.  Perhitungan Retribusi <br>
													&emsp;&emsp;4.  Penerbitan PBG <br><br>
													PBG berlaku sekali seumur hidup bangunan yang bersangkutan, untuk memulai mengajukan PBG <a data-toggle="modal" data-target="#Loginnya"><b>klik disini</b></a>
